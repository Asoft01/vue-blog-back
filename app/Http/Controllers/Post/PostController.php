     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $rules = [
            'title' => 'required',
            'sub_content' => 'required',
            'content' => 'required'
        ];

        $this->validate($request, $rules);

        $data= $request->all();
        $post = Post::create($data);
        return ['post' => $post];
    }

